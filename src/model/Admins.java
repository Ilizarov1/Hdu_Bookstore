package model;

public class Admins
{
    private String adminName;
    private String pwd;

    public String getAdminName()
    {
        return adminName;
    }

    public String getPwd()
    {
        return pwd;
    }

    public void setPwd(String pwd)
    {
        this.pwd = pwd;
    }

    public void setAdminName(String adminName)
    {
        this.adminName = adminName;
    }
}
